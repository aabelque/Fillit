#include "fillit.h"

void	t_add(char **sq, int coord[4][4], int p)
{
	int i;
	int x;
	int y;

	i = -1;
	x = 0;
	y = 0;
	while (++i <= 3)
	{
		y = coord[i][0];
		x = coord[i][1];
		sq[y][x] = 'A' + p;
	}
}
