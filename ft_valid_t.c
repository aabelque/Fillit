#include "fillit.h"

int		ft_valid_t(char **s)
{
	int	i;

	i = -1;
	while (s[++i] != '\0')
	{
		if ((ft_tetr_v(s[i])) == 0)
			return (0);
	}
	return (1);
}

int		ft_tetr_v(char *s)
{
	int	u;
	int	i;

	u = 0;
	i = 0;
	while (i < 20)
	{
		if (s[i] == '#')
		{
			if ((i + 1) < 20 && s[i + 1] == '#')
				u++;
			if ((i - 1) >= 0 && s[i - 1] == '#')
				u++;
			if ((i + 5) < 20 && s[i + 5] == '#')
				u++;
			if ((i - 5) >= 0 && s[i + 5] == '#')
				u++;
		}
		i++;
	}
	return (u == 6 || u == 8);
}
