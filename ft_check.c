#include "fillit.h"

int		ft_check(int dest[4][4], char **sq)
{
	int i;

	i = 0;
	while (i < 4)
	{
		if (dest[i][1] > g_size - 1 || dest[i][1] < 0)
			return (0);
		if (dest[i][0] > g_size - 1 || dest[i][0] < 0 ||
				sq[dest[i][0]][dest[i][1]] != '.')
			return (0);
		i++;
	}
	return (1);
}
