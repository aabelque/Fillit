#include "fillit.h"

void	ft_print_m(char **map)
{
	int	i;
	int j;

	i = -1;
	while (map[++i] != 0)
	{
		j = -1;
		while (map[i][++j] != '\0')
			write(1, &map[i][j], 1);
		write(1, "\n", 1);
	}
}
